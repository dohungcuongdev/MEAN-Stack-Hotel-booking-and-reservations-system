function convertDate(originaldate) {
    var temp1 = originaldate.substring(0, 10);
    var temp2 = originaldate.substring(10, 15);
    var temp3 = originaldate.substring(15, 24);
    return new Date( temp1 + temp2 + temp3 );
}

